public class Datatypes {
    public static void main(String[] args) {
        int x=500;
        int y=7;
        byte m=(byte)x;
        System.out.println("x is:"+x+m);
        System.out.println("y is:"+y);
        byte b=(byte)1000;
        int k=(int)x+b;
        System.out.println("b is:"+b+k);
    }
}